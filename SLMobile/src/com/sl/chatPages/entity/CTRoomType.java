package com.sl.chatPages.entity;

public class CTRoomType
{
	private int id;
	private String name;
	private short dr;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public short getDr()
	{
		return dr;
	}

	public void setDr(short dr)
	{
		this.dr = dr;
	}

}
